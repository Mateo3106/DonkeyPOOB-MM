package Aplicacion;
/**
 * clase escalera por la que se podran movilizar los barriles y los rescatadores
 *
 */
public class escalera {
	public int posX1;
	public int posY1;
	public int posY2;
	
	/**
	 * creador de la clase escalera
	 * @param x en que posicion x se encuentra la escalera
	 * @param y1 posicion y inicial
	 * @param y2 posicion y final
	 */
	public escalera(int x, int y1, int y2){
		posX1=x;
		posY1=y1;
		posY2=y2;
	}
		
}
